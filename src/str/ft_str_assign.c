////////////////////////////////////////////////////////////////////////////////

#include "libft.h"

////////////////////////////////////////////////////////////////////////////////

t_success	ft_str_assign(char **dst, char *src)
{
	if (src == NULL)
		return (ERROR);
	*dst = src;
	return (SUCCESS);
}

