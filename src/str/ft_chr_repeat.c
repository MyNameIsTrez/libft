#include "libft.h"

////////////////////////////////////////////////////////////////////////////////

/**
 * @brief Returns @p chr repeated @p repeat times.
 *
 * @param str
 * @param repeat
 * @return
 */
char	*ft_chr_repeat(const char chr, size_t repeat)
{
	char	*str;

	str = ft_stralloc(repeat);
	ft_memset(str, chr, repeat);
	return (str);
}

