#include "libft.h"

////////////////////////////////////////////////////////////////////////////////

#include "ft_deque_struct.h"

////////////////////////////////////////////////////////////////////////////////

void	*ft_deque_at(t_deque *deque, size_t index)
{
	return (((t_u8 *)deque->data) + (deque->start_index + index) * deque->element_size);
}

