#include "libft.h"

////////////////////////////////////////////////////////////////////////////////

#include "ft_malloced.h"

////////////////////////////////////////////////////////////////////////////////

void	ft_free_mallocs(void)
{
	t_malloced	*malloced;
	size_t		index;

	malloced = get_malloced();
	if (malloced == NULL)
		return ;
	index = 0;
	while (index < malloced->size)
	{
		free(malloced->malloc_ptrs[index]);
		index++;
	}
	free(malloced->malloc_ptrs);
}

