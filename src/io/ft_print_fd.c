#include "libft.h"

////////////////////////////////////////////////////////////////////////////////

#include <stdlib.h>
#include <unistd.h>

////////////////////////////////////////////////////////////////////////////////

// TODO: Make this work for any special characters, not just \n
void	ft_print_fd(char *str, int fd)
{
	char	*replaced;

	replaced = ft_str_replace(str, "\n", "\\n");
	write(fd, replaced, ft_strlen(replaced) + 1);
	free(replaced);
}

