/**
 * @brief Writes a character.
 *
 * @param chr The character to output.
 * @param fd The file descriptor to write to.
 */
ssize_t	ft_putchar_fd(char chr, int fd)
{
	return (ft_unstable_write(fd, &chr, 1));
}

