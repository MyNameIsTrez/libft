Test(ft_atoi)
{
	massert(ft_atoi(" 12345 "), atoi(" 12345 "));
	massert(ft_atoi("- 12345"), atoi("- 12345"));
	massert(ft_atoi("+ 12345"), atoi("+ 12345"));
}

