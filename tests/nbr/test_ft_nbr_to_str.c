#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

void	test_ft_nbr_to_str(void)
{
	ASSERT_STR(ft_nbr_to_str(42, 10), "42");
	ASSERT_STR(ft_nbr_to_str(-42, 10), "-42");
	ASSERT_STR(ft_nbr_to_str(0, 10), "0");
	ASSERT_STR(ft_nbr_to_str(-2147483648, 10), "-2147483648");
}

