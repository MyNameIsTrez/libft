Test(ft_max)
{
	ASSERT(ft_max(1, 2), 2);
	ASSERT(ft_max(2, 2), 2);
	ASSERT(ft_max(3, 2), 3);
	ASSERT(ft_max(-1, -2), -1);
	ASSERT(ft_max(-2, -2), -2);
	ASSERT(ft_max(-3, -2), -2);
	ASSERT(ft_max(0, -1), 0);
	ASSERT(ft_max(0, 0), 0);
}

