#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_is_upper)
{
	massert(ft_is_upper('A'), (bool)true);
	massert(ft_is_upper('A'), (bool)true);

	massert(ft_is_upper('a'), (bool)false);
	massert(ft_is_upper('z'), (bool)false);
	massert(ft_is_upper('#'), (bool)false);
}

