Test(ft_char_to_str)
{
	ASSERT(ft_str_eq(ft_char_to_str('c'), "c"), (bool)true);
	ASSERT(ft_str_eq(ft_char_to_str('\0'), ""), (bool)true);
	ASSERT(ft_char_to_digit(10 + '0'), -1);
}

