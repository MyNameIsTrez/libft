Test(ft_tolower)
{
	ASSERT(ft_tolower('A'), 'a');
	ASSERT(ft_tolower('Z'), 'z');
	ASSERT(ft_tolower('a'), 'a');
	ASSERT(ft_tolower('z'), 'z');
	ASSERT(ft_tolower('#'), '#');
}

