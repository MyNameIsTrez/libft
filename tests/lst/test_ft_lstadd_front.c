void	test_ft_lstadd_front(void)
{
	t_list	*lst;

	lst = NULL;

	ft_lstadd_front(&lst, ft_lstnew("foo"));
	ft_lstadd_front(&lst, ft_lstnew("foo"));

	ASSERT(ft_lstsize(lst), 2);

	ft_lstclear(&lst, NULL);
}

