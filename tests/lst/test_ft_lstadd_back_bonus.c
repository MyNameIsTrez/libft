void	test_ft_lstadd_back(void)
{
	t_list	*lst;

	lst = NULL;

	ft_lstadd_back(&lst, ft_lstnew("foo"));
	ft_lstadd_back(&lst, ft_lstnew("foo"));

	ASSERT_INT(ft_lstsize(lst), 2);

	ft_lstclear(&lst, NULL);
}

