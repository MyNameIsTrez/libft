#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_lstclear)
{
	t_list	*lst;

	lst = NULL;

	ft_lst_new_back(&lst, strdup("foo"));
	ft_lst_new_back(&lst, strdup("bar"));
	ft_lst_new_back(&lst, strdup("baz"));

	ft_lstclear(&lst, &free);
	ASSERT((void *)lst, NULL);
}

