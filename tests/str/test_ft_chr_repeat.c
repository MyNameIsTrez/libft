#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_chr_repeat)
{
	massert(ft_chr_repeat('\0', 0), "");
	massert(ft_chr_repeat('a', 0), "");

	massert(ft_chr_repeat('\0', 1), "");
	massert(ft_chr_repeat('a', 1), "a");

	massert(ft_chr_repeat('\0', 2), "");
	massert(ft_chr_repeat('a', 2), "aa");
}

