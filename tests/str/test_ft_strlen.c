Test(ft_strlen)
{
	ASSERT(ft_strlen(""), strlen(""));
	ASSERT(ft_strlen("f"), strlen("f"));
	ASSERT(ft_strlen("fo"), strlen("fo"));
	ASSERT(ft_strlen("foo"), strlen("foo"));
}

