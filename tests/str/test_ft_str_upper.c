////////////////////////////////////////////////////////////////////////////////

#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

void	test_ft_str_upper(void)
{
	ASSERT(ft_str_upper(strdup("")), "");
	ASSERT(ft_str_upper(strdup("foo")), "FOO");
	ASSERT(ft_str_upper(strdup("BAR")), "BAR");
	ASSERT(ft_str_upper(strdup("$")), "$");
}

