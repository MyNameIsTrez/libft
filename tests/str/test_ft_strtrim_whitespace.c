#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_strtrim_whitespace)
{
	massert(ft_strtrim_whitespace(" a"), "a");
	massert(ft_strtrim_whitespace(" \n\t foo bar \n\t "), "foo bar");
	massert(ft_strtrim_whitespace("\t\n\r\v\f  469 \n"), "469");
}

