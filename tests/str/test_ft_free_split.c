#include "tests.h"

////////////////////////////////////////////////////////////////////////////////

Test(ft_free_split)
{
	char	*str = "AB1ABAB2AB3";
	char	*sep = "AB";
	char	**split_str = ft_split_str(str, sep);

	ft_free_split(&split_str);
	ASSERT((void *)split_str, NULL);
}

