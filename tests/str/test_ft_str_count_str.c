Test(ft_str_count_str)
{
	massert(ft_str_count_str("foo", ""), (size_t)0);
	massert(ft_str_count_str("foo", "o"), (size_t)2);
	massert(ft_str_count_str("fooo", "oo"), (size_t)1);
}

