Test(ft_str_not_chr)
{
	massert(ft_str_not_chr("bonjour", '\0'), "bonjour");
	massert(ft_str_not_chr("bonjour", 'b'), "onjour");
	massert(ft_str_not_chr("bonjour", 'o'), "bonjour");
}

