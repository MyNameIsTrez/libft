Test(ft_strrchr)
{
	massert((void *)ft_strrchr("bonjour", 's'), (void *)strrchr("bonjour", 's'));
	massert((void *)ft_strrchr("bonjour", 'j'), (void *)strrchr("bonjour", 'j'));
}

